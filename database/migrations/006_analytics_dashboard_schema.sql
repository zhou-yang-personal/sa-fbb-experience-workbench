-- Analytics cockpit dedicated ADS schema.
-- Keep dashboard queries on ADS/DWS instead of RAW.

CREATE TABLE IF NOT EXISTS ads_dashboard_kpi_summary (
  analysis_run_id VARCHAR(64) NOT NULL,
  import_batch_id VARCHAR(64) NOT NULL,
  kpi_group VARCHAR(64) NOT NULL DEFAULT 'overview',
  kpi_key VARCHAR(128) NOT NULL,
  kpi_label VARCHAR(255) NOT NULL,
  kpi_value DECIMAL(24,6) NULL,
  kpi_unit VARCHAR(32) NULL,
  kpi_hint VARCHAR(512) NULL,
  display_order INT NOT NULL DEFAULT 0,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (analysis_run_id, kpi_key),
  INDEX ix_batch_group (import_batch_id, kpi_group, display_order)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS ads_app_experience_rank (
  analysis_run_id VARCHAR(64) NOT NULL,
  import_batch_id VARCHAR(64) NOT NULL,
  app_category VARCHAR(64) NOT NULL,
  app_name VARCHAR(255) NOT NULL,
  user_type VARCHAR(32) NOT NULL DEFAULT 'ALL',
  active_users BIGINT NOT NULL DEFAULT 0,
  traffic_gb DECIMAL(24,6) NOT NULL DEFAULT 0,
  duration_hours DECIMAL(24,6) NOT NULL DEFAULT 0,
  avg_effective_mbps DECIMAL(18,6) NULL,
  avg_vmos DECIMAL(18,6) NULL,
  avg_mos DECIMAL(18,6) NULL,
  avg_subscriber_rtt_ms DECIMAL(18,6) NULL,
  avg_network_rtt_ms DECIMAL(18,6) NULL,
  avg_user_loss_pct DECIMAL(18,6) NULL,
  avg_network_loss_pct DECIMAL(18,6) NULL,
  poor_experience_users BIGINT NOT NULL DEFAULT 0,
  poor_experience_user_pct DECIMAL(18,6) NULL,
  main_issue_driver VARCHAR(128) NULL,
  evidence_summary TEXT NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (analysis_run_id, app_category, app_name, user_type),
  INDEX ix_app_rank (analysis_run_id, traffic_gb, active_users),
  INDEX ix_batch_app (import_batch_id, app_category, user_type)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS ads_hourly_experience_trend (
  analysis_run_id VARCHAR(64) NOT NULL,
  import_batch_id VARCHAR(64) NOT NULL,
