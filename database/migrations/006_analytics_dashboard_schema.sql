CREATE TABLE IF NOT EXISTS ads_user_experience_profile (
  analysis_run_id VARCHAR(64) NOT NULL,
  import_batch_id VARCHAR(64) NOT NULL,
  user_key VARCHAR(255) NOT NULL,
  user_type VARCHAR(32) NULL,
  active_days BIGINT NOT NULL DEFAULT 0,
  active_hours BIGINT NOT NULL DEFAULT 0,
  total_traffic_gb DECIMAL(24,6) NOT NULL DEFAULT 0,
  video_traffic_gb DECIMAL(24,6) NOT NULL DEFAULT 0,
  game_hours DECIMAL(24,6) NOT NULL DEFAULT 0,
  top_app_category VARCHAR(64) NULL,
  avg_effective_mbps DECIMAL(18,6) NULL,
  avg_vmos DECIMAL(18,6) NULL,
  avg_mos DECIMAL(18,6) NULL,
  avg_subscriber_rtt_ms DECIMAL(18,6) NULL,
  avg_network_rtt_ms DECIMAL(18,6) NULL,
  avg_user_loss_pct DECIMAL(18,6) NULL,
  avg_network_loss_pct DECIMAL(18,6) NULL,
